int	ft_strlen(char *str)
{
	int	len;

	len = 0;
	while (str[len])
		len++;
	return (len);
}

int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	max_len;

	i = 0;
	max_len = ft_strlen(s1);
	if (max_len < ft_strlen(s2))
		max_len = ft_strlen(s2);
	while (i < max_len)
	{
		if (s1[i] > s2[i])
			return (1);
		else if (s1[i] < s2[i])
			return (-1);
		i++;
	}
	return (0);
}
