unsigned int	ft_strlen(char *str)
{
	unsigned int	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;
	unsigned int	max_len;

	max_len = ft_strlen(s1);
	if (max_len < ft_strlen(s2))
		max_len = ft_strlen(s2);
	i = 0;
	while (i < n && i < max_len)
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (0);
}
